import logging
import azure.functions as func
from PIL import Image
from io import BytesIO

def main(req: func.HttpRequest) -> func.HttpResponse:
    logging.info("üü° Funci√≥n Azure iniciada: transformaci√≥n de imagen JPG a TIFF")

    try:
        file_bytes = req.get_body()
        logging.info("üì• Bytes recibidos. Tama√±o: %d bytes", len(file_bytes))

        if not file_bytes:
            logging.warning("‚ö†Ô∏è No se recibi√≥ ning√∫n cuerpo en la petici√≥n")
            return func.HttpResponse(
                "No se ha enviado ninguna imagen",
                status_code=400
            )

        image = Image.open(BytesIO(file_bytes))
        logging.info("üñºÔ∏è Imagen JPG cargada correctamente")

        output_stream = BytesIO()
        image.save(output_stream, format='TIFF')
        output_stream.seek(0)
        logging.info("‚úÖ Imagen convertida a TIFF en memoria")

        return func.HttpResponse(
            output_stream.read(),
            mimetype="image/tiff",
            status_code=200
        )

    except Exception as e:
        logging.error(f"‚ùå Error al procesar la imagen: {e}", exc_info=True)
        return func.HttpResponse(
            f"Error interno: {str(e)}",
            status_code=500
        )
